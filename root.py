num = int(input("Enter a number to know it's root : "))
root = num**0.5

print("The root of ",num, "is ",root)

num1 = int(input("Enter a number to know it's cuberoot : "))
cubero = num1**0.666666666666666667
cuberoot = cubero**0.5
#Here 0.666667 can be writen as, 2/3 and 0.5 can be writen as 1/2. If we multiply, 2/3 and 1/2 then we will get 1/3, which ultimately gives perfect cuberoots.

print("The root of ",num1, "is ",cuberoot)
